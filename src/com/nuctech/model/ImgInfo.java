package com.nuctech.model;

public class ImgInfo {

	private String id;
	private byte[] zips;
	public byte[] getZips() {
		return zips;
	}
	public void setZips(byte[] zips) {
		this.zips = zips;
	}
	private String state;
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	

}
